Desenvolva uma aplicação utilizando Next.js
 no frontend e
  Node.js no backend para simular um processo simples de pagamento em um e-commerce.

Requisitos Funcionais:

1. Tela Inicial (Carrinho de Compras)
Exiba uma interface simulando um carrinho de compras com um botão "Comprar".


2. Escolha do Meio de Pagamento
Ao clicar no botão "Comprar", deve ser exibida uma modal ou nova seção com duas opções de pagamento:

PIX

Cartão de Crédito



3. Pagamento via PIX
Ao selecionar a opção PIX, a aplicação deve:

Gerar e exibir um QR Code simulado (pode ser um valor fixo ou gerado aleatoriamente).

Informar que o pagamento deve ser feito escaneando o código.



4. Pagamento via Cartão de Crédito
Ao selecionar a opção Cartão de Crédito, a aplicação deve:

Exibir um formulário para preenchimento dos dados do cartão (nome, número, validade e CVV).

Verificar se os dados estão preenchidos corretamente.

Exibir um botão "Pagar".

Ao clicar em "Pagar", exibir uma mensagem de sucesso ou erro 






